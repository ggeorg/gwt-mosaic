package gwt.mosaic.client.ui;

/**
 * Enumeration representing horizontal alignment values.
 */
public enum HorizontalAlignment {
	/**
	 * Align to the right.
	 */
	RIGHT,

	/**
	 * Align to the left.
	 */
	LEFT,

	/**
	 * Align to center.
	 */
	CENTER,

	/**
	 * Align to start.
	 */
	START,

	/**
	 * Align to end.
	 */
	END
}
